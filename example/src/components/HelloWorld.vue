<script lang="ts">
import { ref, defineComponent, Ref, ComponentOptionsMixin } from 'vue'

interface Props {
  msg: {
    type: StringConstructor
    required: true
  }
}

interface Bindings {
  count: Ref<number>
}

export default defineComponent<Props, Bindings, unknown, {}, {}, ComponentOptionsMixin, ComponentOptionsMixin, Record<string, any>, string>({
  name: 'HelloWorld',
  props: {
    msg: {
      type: String,
      required: true
    }
  },
  setup: () => {
    const count = ref(0)
    return { count }
  },
  watch: {
    [nameof<Props>((o) => o.msg)](value, oldValue) {
      console.log(`New msg value is ${value}, previous was ${oldValue}`)
    }
  }
})
</script>

